package com.alibaba.csp.sentinel.dashboard.rule.nacos;

import com.alibaba.csp.sentinel.dashboard.datasource.entity.rule.FlowRuleEntity;
import com.alibaba.csp.sentinel.datasource.Converter;
import com.alibaba.fastjson.JSON;
import com.alibaba.nacos.api.PropertyKeyConst;
import com.alibaba.nacos.api.config.ConfigFactory;
import com.alibaba.nacos.api.config.ConfigService;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.List;
import java.util.Properties;

/**
 * @author Eric Zhao
 * @since 1.4.0
 */
@Configuration
public class NacosConfig {

    @Bean
    public Converter<List<FlowRuleEntity>, String> flowRuleEntityEncoder() {
        return JSON::toJSONString;
    }

    @Bean
    public Converter<String, List<FlowRuleEntity>> flowRuleEntityDecoder() {
        return s -> JSON.parseArray(s, FlowRuleEntity.class);
    }

    @Bean
    public ConfigService nacosConfigService() throws Exception {
        Properties properties = new Properties();
        //nacos配置中心地址
        properties.put(PropertyKeyConst.SERVER_ADDR, "192.168.100.80:8848");
        // nacos配置中心的namespace
//        properties.put(PropertyKeyConst.NAMESPACE, "e95689b2-72d7-457e-b99b-231c1390be53");//dev
        properties.put(PropertyKeyConst.NAMESPACE, "a5a8124b-0588-457e-a54d-b533c1355cc7");//local
//        properties.put(PropertyKeyConst.NAMESPACE, "b5dff6a0-a53c-45d1-8d9e-f46fc496e42d");//prod
        return ConfigFactory.createConfigService(properties);

    }
}
